/**
 * Translate HTML content based on language
 */
export function translateContent(htmlContent, targetLanguage) {
  if (!htmlContent || targetLanguage === "en") {
    console.log("⏭️ Skipping translation - no content or English language");
    return htmlContent;
  }

  console.log("🔧 Starting translation process...");

  try {
    // Sort translations by length (longest first) to avoid partial replacements
    const sortedTranslations = Object.entries(translations).sort(
      ([a], [b]) => b.length - a.length
    );

    console.log(`📚 Total translation entries: ${sortedTranslations.length}`);

    // Create a temporary DOM element to parse HTML
    const parser = new DOMParser();
    const doc = parser.parseFromString(htmlContent, "text/html");
    const body = doc.body;

    let translationCount = 0;

    // Function to translate text nodes only
    function translateNode(node) {
      if (node.nodeType === Node.TEXT_NODE) {
        let text = node.textContent;

        // Skip if empty or just whitespace
        if (!text.trim()) return;

        const originalText = text;

        // Apply translations
        sortedTranslations.forEach(([englishText, langTranslations]) => {
          if (langTranslations[targetLanguage]) {
            // Create regex to match whole words/phrases
            const regex = new RegExp(
              `\\b${englishText.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`,
              "gi"
            );
            const newText = text.replace(
              regex,
              langTranslations[targetLanguage]
            );
            if (newText !== text) {
              translationCount++;
              text = newText;
            }
          }
        });

        if (originalText !== text) {
          node.textContent = text;
        }
      } else if (node.nodeType === Node.ELEMENT_NODE) {
        // Don't translate script or style tags
        if (node.tagName !== "SCRIPT" && node.tagName !== "STYLE") {
          // Recursively translate child nodes
          Array.from(node.childNodes).forEach(translateNode);
        }
      }
    }

    // Translate all text nodes
    translateNode(body);

    console.log(
      `✨ Translation complete! ${translationCount} replacements made`
    );

    // Return the translated HTML - get all body content
    return body.innerHTML;
  } catch (error) {
    console.error("❌ Translation error:", error);
    return htmlContent; // Return original content on error
  }
}

/**
 * Check if content needs translation
 */
export function needsTranslation(language) {
  return language && language !== "en";
}

export default {
  translateContent,
  needsTranslation,
  translations,
};
